package com.sodimac.automation.model;

public class EconomyActivity {
	public int economy_activity_id;
	public int economy_activity_code;
	public String type;
	public String country;
	public int getEconomy_activity_id() {
		return economy_activity_id;
	}
	public void setEconomy_activity_id(int economy_activity_id) {
		this.economy_activity_id = economy_activity_id;
	}
	public int getEconomy_activity_code() {
		return economy_activity_code;
	}
	public void setEconomy_activity_code(int economy_activity_code) {
		this.economy_activity_code = economy_activity_code;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	

}
